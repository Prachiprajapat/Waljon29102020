package com.iwtechnocrat.waljon.Model;

public class ShowAddressModel {
    String id,name,houseno,locality,district,pincode,state,mobile,type;

    public ShowAddressModel(String id, String name, String houseno, String locality, String district, String pincode, String state, String mobile, String type) {
        this.id = id;
        this.name = name;
        this.houseno = houseno;
        this.locality = locality;
        this.district = district;
        this.pincode = pincode;
        this.state = state;
        this.mobile = mobile;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHouseno() {
        return houseno;
    }

    public void setHouseno(String houseno) {
        this.houseno = houseno;
    }

    public String getLocality() {
        return locality;
    }

    public void setLocality(String locality) {
        this.locality = locality;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getPincode() {
        return pincode;
    }

    public void setPincode(String pincode) {
        this.pincode = pincode;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
